require 'prime_factors'

describe PrimeFactors do

[
  [600851475143, [71, 839, 1471, 6857]],
  [1, []],
  [2, [2]],
  [3, [3]],
  [4, [2,2]],
  [5, [5]],
  [6, [2,3]],
  [7, [7]],
  [8, [2,2,2]],
  [9, [3,3]],
  [10, [2,5]],
  [48, [2,2,2,2,3]],
  [5*5*5*3*2, [5,5,5,3,2].sort],
  [323*323*323*323, [17,17,17,17,19,19,19,19].sort],
  [2*3*5*7*11*13*13, [2,3,5,7,11,13,13]],
  [2**19-1, [2**19-1]],
  [8191*131071, [8191,131071]],
  [2**100, [2]*100],
  [2**100000, [2]*100000],

].each do |value, factors|
    it 'returns #{factors} when #{value} is input' do
      subject.factor(value).should == factors
    end
  end

end